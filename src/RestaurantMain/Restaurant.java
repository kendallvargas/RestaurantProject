    private void agregarProductoFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarProductoFinalActionPerformed
        boolean foundProduct = false;

        try {

            Product product1;

            // convert the return of the .getValue() object to an int
            int value = (int) spinnerCantProd.getValue();

            String productName = fieldNombreProducto.getText();

            // validation so that it does not enter when the list is empty
            if (!listProducts.isEmpty()) {

                for (Product list : listProducts) {

                    if (productName.equals(list.getProductName())) {

                        // I multiply the quantity by the price of that product consumed
                        double result = list.getProductPrice() * value;

                        // creating a new object of type Product but this time storing the price consumed
                        product1 = new Product(productName, result);
                        // Adding the product with the price based on consumption, in the cart array
                        cartProducts.add(product1);
                        foundProduct = true;

                        // filling out the model's table
                        updateCartTable();

                        break;
                    }

                    if (foundProduct) {
                        break;
                    }

                }

                if (!foundProduct) {
                    JOptionPane.showMessageDialog(this, "Product not found!!");

                }

                order = new Order();

                order.setDate(formattedDate);
                // set what is missing from the Order constructor, the 2 arrayLists
                order.setProductList(listProducts);
                order.setProductCart(cartProducts);

            } else {
                JOptionPane.showMessageDialog(this, "Products are needed before placing an order!");
            }
        } catch (Exception e) {
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(this, "Error!");

        }

    }//GEN-LAST:event_agregarProductoFinalActionPerformed

