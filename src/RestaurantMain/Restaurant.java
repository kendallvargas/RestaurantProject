    private void btnAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCarritoActionPerformed

        try {
            order.setClientName(fieldDuenioCuenta.getText());
            double discount = 0;
            String discountString = fieldDescuento.getText();
            discount = switch (discountString) {
                case "DSC5" ->
                0.05;
                case "DSC10" ->
                0.10;
                case "DSC15" ->
                0.15;
                default ->
                0;
            };

            // Setting the order number automatically
            String orderStart = "ORD #";
            int iterator = countIndex();
            String iteratorString = String.valueOf(iterator);
            String orderFinal = orderStart + iteratorString;
            order.setOrderNumber(orderFinal);

            // setting the discount
            order.setDiscount(discount);

            if (order.getClientName().isBlank() || discountString.isBlank() || order.getProductCart().isEmpty()) {
                JOptionPane.showMessageDialog(this, "INVALID DATA, TRY AGAIN!");
            } else {
                // This saves all the reports in an array so taht I can display them later in menu 3
                totalOrderReport.add(order.totalCalculation());
                btnGenerarReporte.setEnabled(true);

                // This saves the individual report of each order in a variable
                String report = order.totalCalculation();
                // This sets the report to a text area.
                textArea.setText(report);
            }

        } catch (Exception e) {
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }//GEN-LAST:event_btnAgregarCarritoActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try {
            Product prod = new Product();
            String nombreProducto = fieldNombre.getText();
            boolean duplicateProduct = false;
            
            for (Product listProduct : listProducts) {
                if(listProduct.getProductName().equals(nombreProducto)) {
                    duplicateProduct = true;
                    break;
                }
            }
            
            // check if the product is already added or not, if it is, it will show an error message and won't add the product again
            if(!duplicateProduct){
                prod.setProductName(fieldNombre.getText());
                prod.setProductPrice(Double.parseDouble(fieldPrecio.getText()));

                // add the product to the arrayList
                listProducts.add(prod);

                // refreshing the table
                updateProductTable(); 
            } else{
                JOptionPane.showMessageDialog(this, "The product already exists!");
            }
   

        } catch (NumberFormatException e) {
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(this, "INVALID DATA, TRY AGAIN!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR ADDING PRODUCT");
            e.printStackTrace(System.out);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

