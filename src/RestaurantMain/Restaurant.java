    public static void fillOrderReport() {

        // here I clean the report so that it does not have the values ​​prior to loading, since when loading the deserialization it loads the variable 'report'
        String report = "";
        for (String reportTotal : totalOrderReport) {
            report += reportTotal;
        }

        // save all report text in GUI areaText variable
        areaTextoReporte.setText(report);
    }

    public static void createJson() {

        // I need to create the file from scratch so that it doesn't throw an exception
        File file = new File(JSON_PRODUCTS);
        if (!file.exists()) {
            try {
                file.createNewFile();
                // write an initial empty JSON
                try (Writer writer = new FileWriter(file)) {
                    writer.write("[]"); // JSON empty
                    fileExistingProduct = false;
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    public static void createJsonOrder() {

        // I need to create the file from scratch so that it doesn't throw an exception
        File file = new File(JSON_ORDERS);
        if (!file.exists()) {
            try {
                file.createNewFile();
                // write an initial empty JSON
                try (Writer writer = new FileWriter(file)) {
                    writer.write("[]"); // JSON empty
                    fileExistingOrder = false;
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    public static void deserializationJson() {
        /* DESERIALIZATION, it is done first because I need to read the JSON
           and transform that data into an object to continue using it */
        if (fileExistingProduct) {
            // only read if the file exists

            // Read JSON from file and deserialize
            try (Reader reader = new FileReader(JSON_PRODUCTS)) {
                // get the type of list being read to be reused and maintain the structure
                java.lang.reflect.Type listType = new TypeToken<ArrayList<Product>>() {
                }.getType();
                listProducts = gson.fromJson(reader, listType);

            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    public static void saveFileTxt() {
        // Save in a txt file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(FILE_PRODUCTS))) {
            // loop through all objects and save them
            for (Product prod : listProducts) {
                String productString = "Product: " + prod.getProductName() + ", " + "Price: " + prod.getProductPrice() + "\n";
                // write the entire string to the file
                writer.write(productString);
                //writer.close();
            }

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public static void serializationJson() {
        // Serialize to JSON and save to json file
        try (Writer writer = new FileWriter(JSON_PRODUCTS)) {
            // This converts the entire array to Json
            gson.toJson(listProducts, writer);

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public static void deserializationString() {
        if (fileExistingProduct) {
            try (Reader reader = new FileReader(JSON_ORDERS)) {
                // get the type of list being read for reuse and maintaining the data structure
                java.lang.reflect.Type listType = new TypeToken<ArrayList<String>>() {
                }.getType();
                // load list by transforming Json file to arrayList
                totalOrderReport = gson.fromJson(reader, listType);

            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    public static void serializationString() {
        try (Writer writer = new FileWriter(JSON_ORDERS)) { // instance to write to file
            // write the order report in json format to .json
            gson.toJson(totalOrderReport, writer);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public static void saveFileOrders() {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(FILE_ORDERS))) {
            String text = "";
            for (String report : totalOrderReport) {
                // Assign the array of strings to a variable
                text = report;
                writer.write(text); // write the array of strings to the .txt file
            }

        } catch (Exception e) {
        }
    }

    // This function is to count the index of the report List and assign this index to the corresponding order number.
    public static int countIndex() {
        int orderNumber = 0;
        for (String report : totalOrderReport) {
            orderNumber++; // count the array of orders to get the index and assign the order # automatically
        }
        // add +1 to the index, so that it matches the current order number
        orderNumber++;
        return orderNumber;
    }

