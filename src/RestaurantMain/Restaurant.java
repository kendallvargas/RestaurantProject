    public void updateProductTable() {

        while (modelProducts.getRowCount() > 0) {
            modelProducts.removeRow(0);
        }

        for (Product listProd : listProducts) {
            Object column[] = new Object[NUM_COLUMN];
            column[0] = listProd.getProductName();
            column[1] = listProd.getProductPrice();

            // I add the columns to the model
            modelProducts.addRow(column);
        }

        // I set this model to my GUI table
        tablaProductos.setModel(modelProducts);

    }

    public void updateCartTable() {

        while (modelCart.getRowCount() > 0) {
            modelCart.removeRow(0);
        }

        for (Product cartList : cartProducts) {
            Object column[] = new Object[NUM_COLUMN];
            column[0] = cartList.getProductName();
            column[1] = cartList.getProductPrice();

            // I add the columns to the model
            modelCart.addRow(column);
        }

        // I set this model to my GUI table
        tablaCarrito1.setModel(modelCart);

    }

